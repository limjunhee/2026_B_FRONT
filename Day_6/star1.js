/*
위쪽 삼각형 찍기(10번 반복)
공백(j) 9 / 별(k) 1
공백 8 / 별 2
공백 7 / 별 3
공백 6 / 별 4
.
.
.
공백 0(첫 반복문 안나옴) / 별 10개 -----> 공백은 9에서 1씩 감소, 별은 1에서 1씩 증가한다.

삼각형 별 찍는 반복문은 최대 별 개수(maxStars)만큼 반복된다.
공백 카운트(j)는 최대 9까지 세며, 최대 별 개수 - 반복한 횟수(i = 1)만큼 공백을 입력한다
반복 횟수(i)가 1씩 증가하며 줄이 바뀔 때마다 공백은 하나씩 줄어들게 된다.
별 개수는 반복 횟수(i)와 똑같은 횟수로 찍는다. 이때, i번째 별, 즉 마지막 별은 뒤에 공백이 없다.
*/


/*
아래쪽 역삼각형 찍기(9번 반복)
공백(j) 1 / 별(k) 9
공백(j) 2 / 별(k) 8
공백(j) 3 / 별(k) 7
.
.
.
공백(j) 9개 / 별(k) 1개 -----> 공백이 1부터 1씩 증가, 별은 9개부터 1개씩 감소

역삼각형 찍는 반복문은 최대 9번 반복되며, 삼각형과 다르게 i의 초기값이 최대 별 개수 - 1이고 루프마다 1씩 감소한다.
공백 카운트(j)는 최대 9까지 증가하며, 초기값 1부터 루프마다 1씩 증가한다. 이때, 공백을 하나씩 늘리기 위해 i가 루프마다 줄어들게 했으므로, 공백이 한 개씩 늘어난다.
역삼각형 별은 최대 별 개수 - 1 부터 1개씩 즐어든다. i가 1씩 줄어들도록 반복문 조건을 선언했기 때문.
*/
let result = "";
const maxStars = 10; // 가운데 줄의 최대 별 개수 (총 10줄)

// 1. 위쪽 삼각형 (1개부터 10개까지 증가)
for (let i = 1; i <= maxStars; i++) {
    // 앞쪽 공백 찍기: 줄이 내려갈수록 9, 8, 7 ... 0개로 줄어듦
    for (let j = 1; j <= maxStars - i; j++) {
        result += " ";
    }

    // 별과 사이 공백 찍기
    for (let k = 1; k <= i; k++) {
        if (k === i) {
            result += "*"; // 마지막 별 뒤에는 공백을 붙이지 않음
        } else {
            result += "* "; // 별과 별 사이의 공백
        }
    }

    result += "\n"; // 줄바꿈
}

// 2. 아래쪽 역삼각형 (9개부터 1개까지 감소)
for (let i = maxStars - 1; i >= 1; i--) {
    // 앞쪽 공백 찍기: 줄이 내려갈수록 1, 2, 3 ... 9개로 늘어남
    for (let j = 1; j <= maxStars - i; j++) {
        result += " ";
    }

    // 별과 사이 공백 찍기
    for (let k = 1; k <= i; k++) {
        if (k === i) {
            result += "*";
        } else {
            result += "* ";
        }
    }

    result += "\n"; // 줄바꿈
}

console.log(result);